    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String function = functionField.getText();
        errorField.setText("");
        double guess =0;
        try {
            guess = Double.parseDouble(guessField.getText());
        } catch (Exception e) {
            errorField.setText("Please enter a guess");
            zeroField.setText("null");
        }

        ZeroCalculator z = new ZeroCalculator();

        double zero = 0;

        try {
            zero = z.zero(function, guess);
             zeroField.setText(String.valueOf(zero));
        }
        catch (Exception e)
        {
            errorField.setText(e.getMessage());
            zeroField.setText("null");
        }

       


    }//GEN-LAST:event_jButton1ActionPerformed

